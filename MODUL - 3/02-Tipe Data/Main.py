# a = 10, maka a adalah variabel yang nilainya 10

# integer
# tipe data : angka satuan yang tidak punya koma
data_integer = 11
print("data: ", data_integer)
# type() berfungsi untuk mengetahui tipe data variabel
print("tipe data :", type (data_integer)) #< class 'int

# floart 
# tipe data : angka dengan koma
data_floart = 3.14
print(" data :", data_floart)
print(" tipe data :", type (data_floart))

# string
# tipe data : kumpulan karakter
data_string = "alqa suka mabuk"
print("data :", data_string)
print(" tipe data :", type (data_string))

# boolean 
# tipe data : true/false
data_boolean = False
print(" data :", data_boolean)
print (" tipe data :", type(data_boolean))